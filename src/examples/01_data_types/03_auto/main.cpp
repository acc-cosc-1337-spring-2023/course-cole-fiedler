#include <iostream>

using std::cout;

int main()
{
    auto num = 5;//c++ understands this is int data type
    auto decimal = 5.555;//c++ understands this is double data type

    cout<<"Num is an integer with the value: " <<num<< "\n";
    cout<<"Decimal is a double with the value: " <<decimal<< "\n";
    return 0;
}